package io;

import com.fasterxml.jackson.annotation.JsonProperty;

import java.util.List;

public class Circle {
    @JsonProperty("x")
    private double  x;
    @JsonProperty("y")
    private double y;
    @JsonProperty("r")
    private double r;

    public Circle(double x, double y, double r) {
        this.x = x;
        this.y = y;
        this.r = r;
    }

    public Circle() {
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getR() {
        return r;
    }

    public void setR(double r) {
        this.r = r;
    }
}
